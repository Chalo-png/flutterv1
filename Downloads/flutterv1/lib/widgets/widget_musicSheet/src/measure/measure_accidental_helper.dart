import '../music_objects/key_signature/key_signature.dart';
import '../music_objects/note/accidentals/accidental.dart';
import '../music_objects/note/note_pitch.dart';

class MeasureAccidentalHelper {
  final KeySignatureType initialKeySignatureType;
  const MeasureAccidentalHelper(this.initialKeySignatureType)
      : accidentalPitches = const {};

  void add(Pitch pitch, Accidental accidentalType) {
    accidentalPitches[pitch] = accidentalType;
  }

  final Map<Pitch, Accidental> accidentalPitches;
}
